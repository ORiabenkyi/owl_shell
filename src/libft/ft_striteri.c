#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	counter;
	size_t	length;

	if (!s || !(*s) || !f)
		return ;
	length = ft_strlen(s);
	counter = 0;
	while (counter < length)
	{
		(*f)(counter, &s[counter]);
		counter++;
	}
}
